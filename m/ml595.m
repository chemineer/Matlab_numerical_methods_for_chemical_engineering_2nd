% conjex.m
f = @(x) 0.5*(x(1).^4-16*x(1).^2+5*x(1)) + 0.5*(x(2).^4-16*x(2).^2+5*x(2));
gf = @(x) [2*x(1).^3-16*x(1)+2.5; 2*x(2).^3-16*x(2)+2.5];
Hf = @(x) [6*x(1).^2 - 16 0;0 6*x(2).^2 - 16];
x0 = [0 0]'; crit = 1e-5;
[xs,fs,k] = conjgropt(f,gf,Hf,x0,crit);
disp('최적점:'), xs

disp('최적점에서의 목적함수 값:'), fs
disp('반복계산 횟수:'), k
